const l10nDir = path.join(process.cwd(), 'l10n');

Object.entries(translations).forEach(([lang, keys]) => {
  const filePath = path.join(l10nDir, `${lang}.json`);
  
  try {
    const existingContent = fs.readFileSync(filePath, 'utf8');
    const existingData = JSON.parse(existingContent);
    
    const updatedData = { ...existingData, ...keys };
    
    fs.writeFileSync(filePath, JSON.stringify(updatedData, null, 2) + '\n', 'utf8');
    console.log(`✅ Updated ${lang}.json`);
  } catch (error) {
    console.error(`❌ Error updating ${lang}.json:`, error.message);
  }
});

console.log('\n✨ Translation keys added successfully!');
